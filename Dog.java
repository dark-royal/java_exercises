public class Dog{

private String name;
private String breed;

public Dog(String name,String breed){
	this.name = name;
	this.breed = breed;
}

public void setDogName(String name){
	this.name = name;
}

public String getDogName(){
	return name;
}
public void setDogBreed(String breed){
	this.breed = breed;
}

public String getDogBreed(){
	return breed;
}








}