public class Triangles{

public static void main(String[] args){


int size = 5;

for(int i = 1; i <= size; i++){

	for(int j = 1; j <= i; j++){
		System.out.print('*');
}

	System.out.println();
}

	System.out.print(' ');

for(int babe = 1; babe <= size; babe++){
	for(int you = 1; you <= babe; you++){
		System.out.println('*');
}
	
	System.out.println();
	System.out.println( babe + 1);


}


}


}